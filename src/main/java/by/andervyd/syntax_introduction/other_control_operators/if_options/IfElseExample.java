package by.andervyd.syntax_introduction.other_control_operators.if_options;

public class IfElseExample {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;

        if (a > b) {
            System.out.println("a > b is true");
        } else {
            System.out.println("a > b is false");
        } // >> a > b is true
    }
}
