package by.andervyd.syntax_introduction.basic_constructions.variables;

public class AssignExample {
    public static void main(String[] args) {

        int varEx1;
        int varEx2;

        varEx1 = 4069;
        varEx2 = varEx1 / 4;

        System.out.println("varEx1 = " + varEx1);
        // >> varEx1 = 4069

        System.out.println("varEx2 = varEx1 / 4 = " + varEx2);
        // >> varEx2 = varEx1 / 4 = 1017
    }
}
